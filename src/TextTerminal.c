static void scrollOneLine( TextTerminal term ) {
    term->frameBuffer->ShiftPixelRowsUp( term->frameBuffer, term->glyphPixelHeight, term->frameBuffer->VerticalPixels - term->glyphPixelHeight, term->glyphPixelHeight );
    term->frameBuffer->FillRowsWith( term->frameBuffer, term->frameBuffer->VerticalPixels - term->glyphPixelHeight - 1, term->glyphPixelHeight, term->backgroundColor );
}

static void advanceCursorOneColumn( TextTerminal term ) {
    if (++term->cursorX >= term->glyphColumnsInFrameBuffer) {
        if (++term->cursorY >= term->glyphRowsInFrameBuffer) {
            scrollOneLine( term );
            term->cursorY = term->glyphRowsInFrameBuffer - 1;
        }
        term->cursorX = 0;
    }
}

static Error PutRune( TextTerminal term, Rune rune ) {
    const uint8_t* glyphBitmap;

    switch (rune) {
        case (Rune)'\t':
            for (int i = 0; i < 4; i++)
                PutRune( term, (Rune)' ' );
            break;

        case (Rune)'\n':
            if (++term->cursorY >= term->glyphRowsInFrameBuffer) {
                scrollOneLine( term );
                term->cursorY = term->glyphRowsInFrameBuffer - 1;
            }

            term->cursorX = 0;
            break;

        case (Rune)'\b':
            break;

        default:
            glyphBitmap = term->font->GlyphBitmapForRune( term->font, rune );

            if (glyphBitmap) {
                term->glyphRenderingDefinition->BitmapDefinition = (uint8_t*)glyphBitmap;
                term->frameBuffer->DrawAligned2ColorBitmapAt( term->frameBuffer, term->cursorY * term->glyphPixelHeight, term->cursorX * term->glyphPixelWidth, term->glyphRenderingDefinition, term->foregroundColor, term->backgroundColor );

                advanceCursorOneColumn( term );
            }

            
    }

    return NoError;
}

static Error PutRuneString( TextTerminal term, const RuneString string ) {
    RuneString r = (RuneString)string;

    while (*r)
        PutRune( term, *r++ );

    return NoError;

}

static Error Clear( TextTerminal term ) {
    term->frameBuffer->FillAllWith( term->frameBuffer, term->backgroundColor );
    term->cursorX = term->cursorY = 0;

    return NoError;

}

static int stringIterativeFormatCallback( RuneStringBuffer inBuffer, __attribute__((unused)) Error e, __attribute__((unused)) int done, void* additionalArgs )
{
    TextTerminal term = (TextTerminal)additionalArgs;
    PutRuneString( term, inBuffer->String );
    return done;
}

static Error PutFormattedRuneString( TextTerminal term, const RuneString format, ... ) {
    va_list fp;
    va_start( fp, format );
    term->stringFormatter->FormatIterativelyIntoBufferUsingExplicitVarags( term->runeStringBuffer, &(term->formatBufferIteratingCallback), format, fp );
    va_end( fp );
    return NoError;
}

Error PopulateTextTerminal( TextTerminal term, FrameBuffer usingFrameBuffer, TextTerminalFixedFont usingFont ) {
    term->frameBuffer = usingFrameBuffer;
    term->font = usingFont;

    term->backgroundColor = usingFrameBuffer->GenerateConcreteColorFromAbstractPalette( FBAP_Black );
    term->foregroundColor = usingFrameBuffer->GenerateConcreteColorFromAbstractPalette( FBAP_White );

    term->glyphPixelWidth  = usingFont->BytesPerGlyphRow * 8;
    term->glyphPixelHeight = usingFont->RowsPerGlyph;
    term->glyphColumnsInFrameBuffer = usingFrameBuffer->HorizontalPixels / term->glyphPixelWidth;
    term->glyphRowsInFrameBuffer    = usingFrameBuffer->VerticalPixels / term->glyphPixelHeight;

    term->cursorX = term->cursorY = 0;

    term->glyphRenderingDefinition = &(term->_glyphRenderingDefinition);
    term->glyphRenderingDefinition->PixelsPerRow = term->glyphPixelWidth;
    term->glyphRenderingDefinition->NumberOfRows = term->glyphPixelHeight;
    term->glyphRenderingDefinition->BitmapDefinition = 0;

    term->processor = &(term->_processor);

    term->stringFormatter = &(term->_stringFormatter);
    PopulateStringFormatter( term->stringFormatter );

    term->runeStringBuffer->String = term->_buffer;
    term->runeStringBuffer->Size   = TextTerminalInteralStringBufferCapacity;

    term->formatBufferIteratingCallback.OnNextFormatChunk = stringIterativeFormatCallback;
    term->formatBufferIteratingCallback.AdditionalArgs = (void*)term;

    term->Clear = Clear;
    term->PutFormattedRuneString = PutFormattedRuneString;
    term->PutRuneString = PutRuneString;
    term->PutRune = PutRune;

    return NoError;
}

