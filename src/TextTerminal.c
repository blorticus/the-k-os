// Returns the number of Runes consumed from 'startOfFormatString' (including Rune at 'startOfFormatString').
// Returns 0 if the format string contains an errant value or is not terminated before the end of the string
// or before consuming 'maximumNumberOfRunesThatIMayConsume';
unsigned int consumeFormatString(struct TextTerminal_formatProcessor_t *fp, const RuneString startOfFormatString) {
    RuneString formatString = startOfFormatString;
    unsigned int runesConsumed;
    Rune currentFlag = U' ';
    int nextRuneShouldBeEqualSign = 0;
    unsigned int i;

    for (runesConsumed = 0; runesConsumed < fp->maximumNumberOfRunesThatIMayConsume && *formatString && *formatString != U'}'; runesConsumed++, formatString++) {
        switch (*formatString)
        {
            case U',':
                if (currentFlag == U' ')
                    return 0;
                else
                {
                    currentFlag = U' ';
                    continue;
                }

            case U'=':
                if (!nextRuneShouldBeEqualSign)
                    return 0;

                nextRuneShouldBeEqualSign = 0;
        }

        switch (currentFlag)
        {
            case U'l':
                fp->mostRecentFormatString.flags |= PF_LENGTH;
                for (i = 0; i < fp->optionProcessingBufferLength && runesConsumed < fp->maximumNumberOfRunesThatIMayConsume && *formatString && *formatString != U'}' && *formatString != U','; runesConsumed++, formatString++, i++)
                    if (*formatString >= U'0' && *formatString <= U'9') {
                        fp->optionProcessingBuffer[i] = *formatString;
                    } else {
                        if (*formatString != U'}' && *formatString != U',')
                            return 0;

                        fp->optionProcessingBuffer[i] = 0;
                        DecimalIntegerAsStringToInt64( fp->optionProcessingBuffer, fp->optionProcessingBufferLength, (int64_t*)&(fp->mostRecentFormatString.length) );
                    }

                break;

            case U't':
                fp->mostRecentFormatString.flags |= PF_TRUNCATE;
                break;

            case U'f':
                fp->mostRecentFormatString.flags |= PF_FILL;
                break;

            case U's':
                fp->mostRecentFormatString.flags |= PF_INT_STORAGE_SIZE;
                break;

            case U'c':
                fp->mostRecentFormatString.flags |= PF_CASE;
                break;

            case U' ':
                switch (*formatString)
                {
                    case U'l':
                    case U't':
                    case U'f':
                    case U's':
                    case U'c':
                        if (formatString[1] != U'=')
                            return 0;
                        
                        nextRuneShouldBeEqualSign = 1;
                        currentFlag = *formatString;
                        break;

                    default:
                        return  0;
                }
            }
    }

    if (*formatString != U'}')
        return 0;

    return runesConsumed;
}


static void scrollOneLine( TextTerminal term ) {
    term->frameBuffer->ShiftPixelRowsUp( term->frameBuffer, term->glyphPixelHeight, term->frameBuffer->VerticalPixels - term->glyphPixelHeight, term->glyphPixelHeight );
    term->frameBuffer->FillRowsWith( term->frameBuffer, term->frameBuffer->VerticalPixels - term->glyphPixelHeight - 1, term->glyphPixelHeight, term->backgroundColor );
}

static void advanceCursorOneColumn( TextTerminal term ) {
    if (++term->cursorX >= term->glyphColumnsInFrameBuffer) {
        if (++term->cursorY >= term->glyphRowsInFrameBuffer) {
            scrollOneLine( term );
            term->cursorY = term->glyphRowsInFrameBuffer - 1;
        }
        term->cursorX = 0;
    }
}

static Error PutRune( TextTerminal term, Rune rune ) {
    const uint8_t* glyphBitmap;

    switch (rune) {
        case (Rune)'\t':
            for (int i = 0; i < 4; i++)
                PutRune( term, (Rune)' ' );
            break;

        case (Rune)'\n':
            if (++term->cursorY >= term->glyphRowsInFrameBuffer) {
                scrollOneLine( term );
                term->cursorY = term->glyphRowsInFrameBuffer - 1;
            }

            term->cursorX = 0;
            break;

        case (Rune)'\b':
            break;

        default:
            glyphBitmap = term->font->GlyphBitmapForRune( term->font, rune );

            if (glyphBitmap) {
                term->glyphRenderingDefinition->BitmapDefinition = (uint8_t*)glyphBitmap;
                term->frameBuffer->DrawAligned2ColorBitmapAt( term->frameBuffer, term->cursorY * term->glyphPixelHeight, term->cursorX * term->glyphPixelWidth, term->glyphRenderingDefinition, term->foregroundColor, term->backgroundColor );

                advanceCursorOneColumn( term );
            }

            
    }

    return NoError;
}

static Error PutRuneString( TextTerminal term, const RuneString string ) {
    RuneString r = (RuneString)string;

    while (*r)
        PutRune( term, *r++ );

    return NoError;

}

static Error Clear( TextTerminal term ) {
    term->frameBuffer->FillAllWith( term->frameBuffer, term->backgroundColor );
    term->cursorX = term->cursorY = 0;

    return NoError;

}

static Error PutFormattedRuneString( TextTerminal term, const RuneString format, ... ) {
    va_list fp;

    va_start( fp, format );

    for (unsigned int i = 0; format[i]; i++) {
        if (format[i] == U'%') {
            i++;
            switch (format[i]) {
                case 0:
                    term->PutRune( term, U'%' );
                    va_end( fp );
                    return NoError;

                case U'%':
                    term->PutRune( term, U'%' );
                    break;

                case U'r':
                case U'd':
                case U'x':
                    term->processor->type = format[i];
                    if (format[i + 1] == U'{')

                default:
                    return ErrorInvalidFormat;
            }
        } else {
            term->PutRune( term, format[i] );
        }
    }

    va_end( fp );
    return NoError;
}

Error PopulateTextTerminal( TextTerminal term, FrameBuffer usingFrameBuffer, TextTerminalFixedFont usingFont ) {
    term->frameBuffer = usingFrameBuffer;
    term->font = usingFont;

    term->backgroundColor = usingFrameBuffer->GenerateConcreteColorFromAbstractPalette( FBAP_Black );
    term->foregroundColor = usingFrameBuffer->GenerateConcreteColorFromAbstractPalette( FBAP_White );

    term->glyphPixelWidth  = usingFont->BytesPerGlyphRow * 8;
    term->glyphPixelHeight = usingFont->RowsPerGlyph;
    term->glyphColumnsInFrameBuffer = usingFrameBuffer->HorizontalPixels / term->glyphPixelWidth;
    term->glyphRowsInFrameBuffer    = usingFrameBuffer->VerticalPixels / term->glyphPixelHeight;

    term->cursorX = term->cursorY = 0;

    term->glyphRenderingDefinition = &(term->_glyphRenderingDefinition);
    term->glyphRenderingDefinition->PixelsPerRow = term->glyphPixelWidth;
    term->glyphRenderingDefinition->NumberOfRows = term->glyphPixelHeight;
    term->glyphRenderingDefinition->BitmapDefinition = 0;

    term->processor = &(term->_processor);

    term->Clear = Clear;
    term->PutFormattedRuneString = PutFormattedRuneString;
    term->PutRuneString = PutRuneString;
    term->PutRune = PutRune;

    return NoError;
}

