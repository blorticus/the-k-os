    uint8* FrameBuffer::Font::getBitmapFor( uint32 codepoint ) {
        if (codepoint > m_chars_defined - 1)
            return (uint8*)NULL;
        else
            return &m_bitmap[m_char_vrez * codepoint];
    }
    
