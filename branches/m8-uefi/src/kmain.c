typedef enum {
    BLACK       = 0x00000000,
    BLUE        = 0x000000ff,
    GREEN       = 0x0000ff00,
    RED         = 0x00ff0000,
} color;


void lfb_fill( u32* lfb_base, u32 pixel_count, color color ) {
    for ( ; pixel_count > 0; pixel_count--)
        *lfb_base++ = (u32)color;
}


void c_putchar( char c, u32* upper_left_pixel, u32 hrez, color bg_color, color fg_color ) {
    int i, j;
    u8 l;
    u32* p = upper_left_pixel;

    for (i = 0; i < 16; i++) {
        l = font_8x16[(u8)c][i];

        for (j = 0; j < 8; j++) {
            if (l & 0x80)
                *p++ = (u32)fg_color;
            else
                *p++ = (u32)bg_color;

            l <<= 1;
        }

        p += hrez - 8;
    }
}


